void solve()
{
    // ai - aj >= bi - bj
    // ai - bi >= aj - bj
    // ci = ai - bi
    // if ci >= all the other cjs, strong vertex detected
    ll n;
    cin >> n;
    vector<ll> a(n), b(n), res;
    for (int i = 0; i < n; i++)
        cin >> a[i];
    for (int i = 0; i < n; i++)
        cin >> b[i];

    vector<pair<ll, ll>> c(n);
    for (int i = 0; i < n; i++)
        c[i].first = a[i] - b[i],
        c[i].second = i + 1;

    sort(c.begin(), c.end());

    // magnitude of ci (strong vertex)
    ll x = c[n - 1].first;
    for (int i = 0; i < n; i++)
    {
        // strong vertex detected (ci >= all the other cjs)
        if (c[i].first == x)
            res.push_back(c[i].second);
    }
    cout << res.size() << endl;
    for (auto &it : c)
    {
        if (it.first == x)
            cout << it.second << " ";
    }
    cout << endl;
}

